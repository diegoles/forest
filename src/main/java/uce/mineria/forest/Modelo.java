package uce.mineria.forest;

public class Modelo {
	private String real;
	private double negativo;
	private double positivo;
	private double neutro;
	private double error;
	public String getReal() {
		return real;
	}
	public void setReal(String real) {
		this.real = real;
	}
	public double getNegativo() {
		return negativo;
	}
	public void setNegativo(double negativo) {
		this.negativo = negativo;
	}
	public double getPositivo() {
		return positivo;
	}
	public void setPositivo(double positivo) {
		this.positivo = positivo;
	}
	public double getNeutro() {
		return neutro;
	}
	public void setNeutro(double neutro) {
		this.neutro = neutro;
	}
	public double getError() {
		return error;
	}
	public void setError(double error) {
		this.error = error;
	}
	
	
}
